// ── Core translation function ────────────────────────────
function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    localStorage.setItem('lang', lang);

    // Update html[lang] attribute for screen-readers / fonts
    document.documentElement.lang = lang;

    // 1) Translate text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key  = el.getAttribute('data-i18n');
        const text = translations[lang][key];
        if (text === undefined) return;

        // Element has child nodes (e.g. icon spans) → only update text nodes
        if (el.childElementCount > 0) {
            // Replace / create a dedicated text-only child span
            let textSpan = el.querySelector('span[data-i18n-text]');
            if (!textSpan) {
                // Remove bare text nodes and collect them
                let combined = '';
                [...el.childNodes].forEach(node => {
                    if (node.nodeType === Node.TEXT_NODE) {
                        combined += node.textContent;
                        node.remove();
                    }
                });
                textSpan = document.createElement('span');
                textSpan.setAttribute('data-i18n-text', '');
                // combined may be empty if no raw text node existed
                el.appendChild(textSpan);
            }
            textSpan.textContent = text;
        } else {
            el.textContent = text;
        }
    });

    // 2) Translate placeholder attributes
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key  = el.getAttribute('data-i18n-placeholder');
        const text = translations[lang][key];
        if (text !== undefined) el.placeholder = text;
    });

    // 3) Sync the language dropdown on this page (if present)
    const sel = document.getElementById('language-select');
    if (sel && sel.value !== lang) sel.value = lang;
}

// ── Bootstrap on every page ──────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
    const lang = localStorage.getItem('lang') || 'en';
    setLanguage(lang);

    const langSelect = document.getElementById('language-select');
    if (langSelect) {
        langSelect.value = lang;
        langSelect.addEventListener('change', e => setLanguage(e.target.value));
    }
});
